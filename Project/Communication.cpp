void Communication::Process (void)
{
    EState eState = getState ();
    switch (eState)
    {
        case EState::eReceive:
        {
            receive ();
            break;
        }
        case EState::eSend:
        {
            send ();
            break;
        }
        default:
        { 
            break;
        }
    }
}

